def findChars(str, ch):
    # return[i for i, letter in enumerate(str) if letter == ch]
    arr=[]
    for i, letter in enumerate(str):
        if letter == ch:
            arr.append(i)
    return arr

def arrToStr(arr, str=''):
    for index in arr:
        str+=f'{index.upper()}  '
    return str

def arrToStr2(arr, str=''):
    for index in arr:
        str+=f'{index.upper()}'
    return str


